// Helper to expand compact format into full problem objects
function expandSheet(sheetKey) {
    const sheet = SHEETS[sheetKey];
    if (!sheet) return [];
    return sheet.problems.map(p => ({
        leetcodeId: p[0],
        title: p[1],
        difficulty: p[2],
        tags: p[3],
        url: `https://leetcode.com/problems/${p[1].toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/-+$/, '')}/`
    }));
}
